import pygame
from circleshape import CircleShape
from constants import LINE_WIDTH, ASTEROID_MIN_RADIUS
from logger import log_event
import random

class Asteroid(CircleShape):
  def __init__(self, x, y, radius):
    super().__init__(x, y, radius)

    
  def draw(self, screen):
    pygame.draw.circle(screen, "white", self.position, self.radius, LINE_WIDTH)

  def update(self, dt):
    self.position += self.velocity * dt

  def split(self):
    self.kill()
    if self.radius <= ASTEROID_MIN_RADIUS:
      return
    
    log_event("asteroid_split")
    rand_angle = random.uniform(20, 50)
    first_asteroid_movement = self.velocity.rotate(rand_angle)
    second_asteroid_movement = self.velocity.rotate(-rand_angle)
    new_rad = self.radius-ASTEROID_MIN_RADIUS

    asteroid1 = Asteroid(self.position.x, self.position.y, new_rad)
    asteroid2 = Asteroid(self.position.x, self.position.y, new_rad)

    asteroid1.velocity = first_asteroid_movement * 1.2
    asteroid2.velocity = second_asteroid_movement * 1.2





    